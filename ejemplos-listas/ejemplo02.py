# Creamos una lista de nombres
nombres = ["Ana", "Juan", "María", "Luis", "Elena"]

# Mostramos la lista
print("Lista de nombres:", nombres)

# Añadimos un nuevo nombre al final de la lista
nombres.append("Pedro")

# Eliminamos el segundo nombre de la lista
del nombres[1]

# Mostramos la lista modificada
print("Lista modificada:", nombres)
